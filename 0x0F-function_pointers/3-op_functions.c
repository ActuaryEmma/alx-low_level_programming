#include <stdio.h>
/**
  * op_add - add int a and b
  * @a: first int
  * @b: second int
  * Return: a + b
  */
int op_add(int a, int b)
{
	return (a + b);
}
/**
  * op_sub - subtract int a and b
  * @a: first int
  * @b: second int
  * Return: a - b
  */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
  * op_mul - multiply a and b
  * @a: first int
  * @b: second int
  * Return: a * b
  */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
  * op_div - divide int a and b
  * @a: first int
  * @b: second int
  * Return: a / b
  */
int op_div(int a, int b)
{
	return (a / b);
}
/**
  * op_mod - reminder of the divison of a and b
  * @a: first int
  * @b: second int
  * Return: a % b
  */
int op_mod(int a, int b)
{
	return (a % b);
}
